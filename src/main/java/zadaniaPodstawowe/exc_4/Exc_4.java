package zadaniaPodstawowe.exc_4;

/**
 * Przygotuj program, który wyświetli na konsoli liczby od 1 do 49. Użyj w tym celu pętli
 * while
 */

public class Exc_4 {
  public static void main(String[] args) {
    int i = 1;
    while (i <= 49) {
      System.out.println("Liczba " + i);
      i++;
    }
  }
}
